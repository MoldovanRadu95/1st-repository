public class Ex2For {
    public static void main(String[] args) {
        int numarRanduri = 4;
        for (int i = 1; i <= numarRanduri; i++) {
            for (int j = 1; j <= 3; j++) {
                System.out.print(i + "" + j);
                System.out.print(" ");
            }

            System.out.println("");
        }
    }
}
